    /**
     * 校验是否是指定的时间格式
     *
     * @param str
     * @param pattern
     * @return
     */
    public static boolean isValidDate(String str, String pattern) {
        boolean convertSuccess = true;
        // 指定日期格式为四位年/两位月份/两位日期，注意yyyy/MM/dd区分大小写；
        SimpleDateFormat format = new SimpleDateFormat(pattern);
        try {
            // 设置lenient为false.
            // 否则SimpleDateFormat会比较宽松地验证日期，比如2007/02/29会被接受，并转换成2007/03/01
            format.setLenient(false);
            format.parse(str);
        } catch (ParseException e) {
            // 如果throw java.text.ParseException或者NullPointerException，就说明格式不对
            convertSuccess = false;
        }
        return convertSuccess;
    }

    public static boolean isRightValidTime(String startTime, String endTime) {
        boolean flag = true;
        if (!StringUtils.isEmpty(startTime)) {
            boolean validDate = DateTimeUtil.isValidDate(startTime, "yyyy-MM-dd HH:mm:ss");
            if (!validDate) {
                flag = false;
            }
        }
        if (!StringUtils.isEmpty(endTime)) {
            boolean validDate = DateTimeUtil.isValidDate(endTime, "yyyy-MM-dd HH:mm:ss");
            if (!validDate) {
                flag = false;
            }
        }
        if (!StringUtils.isEmpty(startTime) && !StringUtils.isEmpty(endTime)) {
            //判断是否是时间格式
//            if (!DateTimeUtil.isDate(startTime) && !DateTimeUtil.isDate(endTime)) {
//                flag=false;
//            }
            //开始时间不能晚于结束时间
            if (DateTimeUtil.StringToDate(startTime, "yyyy-MM-dd HH:mm:ss")
                    .after(DateTimeUtil.StringToDate(endTime, "yyyy-MM-dd HH:mm:ss"))) {
                flag = false;
            }
        }
        return flag;
    }

    /*
     *计算time2减去time1的差值 差值只设置 几天 几个小时 或 几分钟
     * 根据差值返回多长之间前或多长时间后
     * */
    public static String getDistanceTime(long time1, long time2) {
        long day = 0;
        long hour = 0;
        long min = 0;
        long sec = 0;
        long diff;

        if (time1 < time2) {
            diff = time2 - time1;
        } else {
            diff = time1 - time2;
        }
        day = diff / (24 * 60 * 60 * 1000);
        hour = (diff / (60 * 60 * 1000) - day * 24);
        min = ((diff / (60 * 1000)) - day * 24 * 60 - hour * 60);
        sec = (diff / 1000 - day * 24 * 60 * 60 - hour * 60 * 60 - min * 60);
        if (day != 0) return day + "天" + hour + "小时" + min + "分钟" + sec + "秒";
        if (hour != 0) return hour + "小时" + min + "分钟" + sec + "秒";
        if (min != 0) return min + "分钟" + sec + "秒";
        if (sec != 0) return sec + "秒";
        return diff + "毫秒";
//        return "0秒";
    }
